                <p>Ruth Mukuiya Kivoi was a God-fearing, morally upright, talented, and exceptional student. Unfortunately, her life took a tragic turn at the age of 16 when she was struck by meningitis and subsequently suffered a stroke that rendered her paralyzed.</p>
                <p>This unfortunate event shattered Ruth's aspirations of pursuing a career in acting, music, or science, which she had fervently documented in her diary. Furthermore, the once supportive network of relatives, friends, and community members turned their backs on Ruth and her family, subjecting them to social stigma and isolation.</p>
                <p>Grace, burdened with these circumstances, found herself grappling with numerous unanswered questions. She contemplated the plight of other parents and guardians facing similar challenges as her own, pondered the suffering experienced by countless children like her granddaughter Ruth, and wondered how many of these children were confined to their homes due to the weight of societal stigmatization.</p>
                <p>Driven by these profound inquiries, Grace gained a newfound perspective that enabled her to confront her own challenges with resilience. Thus, she embarked on the creation of the Malaika Initiative for People with Disability, a non-governmental organization officially registered under number OP. 218/051/14-0314/9702. The primary objective of this organization was to alleviate the suffering endured by children with disabilities, provide hope to their families, and foster positive change within the wider community.</p>

